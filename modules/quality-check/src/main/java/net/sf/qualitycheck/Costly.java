package net.sf.qualitycheck;

import java.lang.annotation.ElementType;
import java.lang.annotation.Retention;
import java.lang.annotation.RetentionPolicy;
import java.lang.annotation.Target;

/**
 * The <code>Costly</code> annotation specifies that an invocation of this method
 * is costly and should be done as seldom as possible.
 * 
 * This is useful to show that code can be slow if a certain method is used to often.
 */
@Retention(RetentionPolicy.RUNTIME)
@Target(ElementType.METHOD)
public @interface Costly {

}
